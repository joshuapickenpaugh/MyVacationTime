    //Gets weeks, days, and hours:
    public void GetWeeksDaysHours(int intFrstHalf)
    {
        //Local variables:
        int intWeeksCounter = 0;
        int intDaysCounter = 0;        
        double dblHours;        
        String strWeeks;
        String strDays;
        String strHours;     
        
        //Calcuates weeks, days, and hours:
        while (intFrstHalf >= 168)
        {
            intFrstHalf = intFrstHalf - 168;
            intWeeksCounter++;
        }
        while (intFrstHalf >= 24)
        {
            intFrstHalf = intFrstHalf - 24;
            intDaysCounter++;
        }
        dblHours = intFrstHalf;
        
        //Splits the decimal; we only need the whole number:
        strHours = Double.toString(dblHours);
        String[] strSplitHoursDecimal = strHours.split("\\.");
        strHours = strSplitHoursDecimal[0];   
        
        //Converts to strings:
        strWeeks = Integer.toString(intWeeksCounter);
        strDays = Integer.toString(intDaysCounter);
        
        //Displays to text boxes:
        txtWeeks.setText(strWeeks);
        txtDays.setText(strDays);
        txtHours.setText(strHours);
        
    }
 

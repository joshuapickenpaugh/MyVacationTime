    //Calculates the user-input decimal number to individual weeks, days, 
    //hours, and minutes of vacation time:
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         
        //Local variables:
        String strUserInput;
        String strFirstHalf;
        String strSecondHalf;
        double dblFirstHalf;  
        int intFirstHalf;
        
        //Gets user input:
        strUserInput = txtUserInput.getText();
         
        //Split the string:
        String[] strUserInputSplit = strUserInput.split("\\.");      
        
        //Assign each half a String variable:
        strFirstHalf = strUserInputSplit[0];
        strSecondHalf = strUserInputSplit[1];      
        
        //Convert to double:
        intFirstHalf = Integer.parseInt(strFirstHalf);
        
        //Calls function:
        GetWeeksDaysHours(intFirstHalf);   
        //Convert string hundredths of a minute to string actual minutes:
        ConvertToMinutes(strSecondHalf);   
    }//GEN-LAST:event_jButton1ActionPerformed

